package com.lwp.ebook.model;

public class FourItem {
    private int iconId;
    private String title;

    public FourItem(int iconId, String title) {
        this.iconId = iconId;
        this.title = title;
    }

    public int getIconId() {
        return iconId;
    }

    public String getTitle() {
        return title;
    }
}
